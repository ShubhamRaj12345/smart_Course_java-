public class pr1 {
    public static void main(String[] args) {
        for(int i = 1; i<=3;i++)  // ye 3 times chalega
        {
            System.out.println("i = "+i);
            for(int j = 1;j<=i;j++) // ye 6 times chalega
            {

                System.out.println("j = "+j);
            }
        }

    }
}
