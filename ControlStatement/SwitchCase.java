package ControlStatement;

public class SwitchCase {
    public static void main(String[] args) {

        String name="shubham";
        switch (name)
        {
            case "amit" :
                System.out.println("101");
            case "shubham" :
                System.out.println("102");
        }

        int x = 102;
        switch(x)
        {
            case 2 :
                System.out.println(12);
                break;
            case 102 :
                System.out.println(123);
                break;
        }
    }
}
